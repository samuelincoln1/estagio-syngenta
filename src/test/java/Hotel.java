

public class Hotel {
	private double dailyFee;
	private double weekendFee;
	private double rewardDailyFee;
	private double rewardWeekendFee;
	private int rate;
	
	
	public double getDailyFee() {
		return dailyFee;
	}
	public void setDailyFee(double dailyFee) {
		this.dailyFee = dailyFee;
	}
	public double getWeekendFee() {
		return weekendFee;
	}
	public void setWeekendFee(double weekendFee) {
		this.weekendFee = weekendFee;
	}
	public double getRewardDailyFee() {
		return rewardDailyFee;
	}
	public void setRewardDailyFee(double rewardDailyFee) {
		this.rewardDailyFee = rewardDailyFee;
	}
	public double getRewardWeekendFee() {
		return rewardWeekendFee;
	}
	public void setRewardWeekendFee(double rewardWeekendFee) {
		this.rewardWeekendFee = rewardWeekendFee;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
			
	
}
	



	